    // Quiz Logic
    let currentQuiz = null;
    let currentQuestionIndex = 0;
    let score = 0;
    let selectedOption = null;

    function showSection(sectionId) {
        document.querySelectorAll('.content-section').forEach(section => {
            section.classList.remove('active');
        });
        document.querySelectorAll('.nav-tab').forEach(tab => {
            tab.classList.remove('active');
        });
        document.getElementById(sectionId + '-section').classList.add('active');
        document.querySelector(`.nav-tab[onclick="showSection('${sectionId}')"]`)?.classList.add('active');
        document.getElementById('quizScreen').style.display = 'none';
        document.getElementById('resultsScreen').style.display = 'none';
    }

    function startQuiz(topic) {
        currentQuiz = topic;
        currentQuestionIndex = 0;
        score = 0;
        selectedOption = null;
        document.getElementById('courses-section').style.display = 'none';
        document.getElementById('climate-section').style.display = 'none';
        document.getElementById('quizScreen').style.display = 'block';
        document.getElementById('resultsScreen').style.display = 'none';
        loadQuestion();
    }

    function loadQuestion() {
        const questionData = quizData[currentQuiz][currentQuestionIndex];
        document.getElementById('quizTitle').textContent = currentQuiz.charAt(0).toUpperCase() + currentQuiz.slice(1).replace('_', ' ') + ' Quiz';
        document.getElementById('currentQuestion').textContent = currentQuestionIndex + 1;
        document.getElementById('totalQuestions').textContent = quizData[currentQuiz].length;
        document.getElementById('questionText').textContent = questionData.question;
        const optionsContainer = document.getElementById('optionsContainer');
        optionsContainer.innerHTML = '';
        questionData.options.forEach((option, index) => {
            const button = document.createElement('button');
            button.classList.add('option-btn');
            button.textContent = option;
            button.onclick = () => selectOption(index);
            optionsContainer.appendChild(button);
        });
        document.getElementById('nextBtn').disabled = true;
        document.getElementById('progressFill').style.width = `${((currentQuestionIndex + 1) / quizData[currentQuiz].length) * 100}%`;
        document.getElementById('explanationContainer').classList.remove('visible');
        document.getElementById('explanationContainer').innerHTML = '';
    }

    function selectOption(index) {
        const questionData = quizData[currentQuiz][currentQuestionIndex];
        selectedOption = index;
        const buttons = document.querySelectorAll('.option-btn');
        buttons.forEach((button, i) => {
            button.disabled = true;
            if (i === questionData.correct) {
                button.classList.add('correct');
            } else if (i === index && index !== questionData.correct) {
                button.classList.add('incorrect');
            }
            if (i === index) {
                button.classList.add('selected');
            }
        });
        if (index === questionData.correct) {
            score++;
        }
        const explanationContainer = document.getElementById('explanationContainer');
        explanationContainer.textContent = questionData.explanation;
        explanationContainer.classList.add('visible');
        document.getElementById('nextBtn').disabled = false;
    }

    function nextQuestion() {
        currentQuestionIndex++;
        if (currentQuestionIndex < quizData[currentQuiz].length) {
            loadQuestion();
        } else {
            showResults();
        }
    }

    function showResults() {
        document.getElementById('quizScreen').style.display = 'none';
        document.getElementById('resultsScreen').style.display = 'block';
        const percentage = (score / quizData[currentQuiz].length) * 100;
        document.getElementById('scoreText').textContent = `${Math.round(percentage)}%`;
        const scoreCircle = document.getElementById('scoreCircle');
        scoreCircle.style.setProperty('--score-deg', `${(percentage / 100) * 360}deg`);
        scoreCircle.className = 'score-circle';
        if (percentage >= 80) {
            scoreCircle.classList.add('score-high');
            document.getElementById('resultTitle').textContent = 'Great Job!';
            document.getElementById('resultMessage').textContent = 'You have a strong understanding of this topic!';
        } else if (percentage >= 50) {
            scoreCircle.classList.add('score-medium');
            document.getElementById('resultTitle').textContent = 'Good Effort!';
            document.getElementById('resultMessage').textContent = 'You’re on your way to mastering this topic!';
        } else {
            scoreCircle.classList.add('score-low');
            document.getElementById('resultTitle').textContent = 'Keep Learning!';
            document.getElementById('resultMessage').textContent = 'Review the material and try again!';
        }
    }

    function backToTopics() {
        // Reset quiz state
        currentQuiz = null;
        currentQuestionIndex = 0;
        score = 0;
        selectedOption = null;
        // Ensure quiz and results screens are hidden
        document.getElementById('quizScreen').style.display = 'none';
        document.getElementById('resultsScreen').style.display = 'none';
        // Show courses section
        showSection('courses');
        // Ensure courses section is visible
        document.getElementById('courses-section').style.display = 'block';
    }


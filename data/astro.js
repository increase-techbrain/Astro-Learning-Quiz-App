    function updateDateTime() {
        const options = {
            timeZone: 'Africa/Lagos', // WAT is UTC+1, using Lagos as reference
            weekday: 'long',
            year: 'numeric',
            month: 'long',
            day: 'numeric',
            hour: 'numeric',
            minute: 'numeric',
            second: 'numeric',
            hour12: true
        };
        const now = new Date().toLocaleString('en-US', options);
        document.getElementById('datetime').textContent = now;
    }

    // Update immediately and every second
